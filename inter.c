#include <unistd.h>

int main(int ac, char **av)
{
	int	i;
	int	k;
	int	check[242];

	i = 0;
	if (ac == 3)
	{
		while (av[1][i] != '\0')
		{
			k = 0;
			while (av[2][k] != '\0')
			{
				if (av[1][i] == av[2][k])
				{
					if (check[(int)av[1][i]] == 0)
					{
						check[(int)av[1][i]] = 1;
						write (1, &av[1][i], 1);
					}
				}
				k++;
			}
			i++;
		}
	}
	write (1, "\n", 1);
	return (0);
}
