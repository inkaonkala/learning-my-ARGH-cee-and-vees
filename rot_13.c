#include <unistd.h>

void	rot_13(char	c)
{
	if (!c)
		return ;
	if ((c >= 'a' && c <= 'm') || (c >= 'A' && c <= 'M'))
		c += 13;
	else if ((c >= 'n' && c <= 'z') || (c <= 'N' && c >= 'Z'))
		c -= 13;
	write (1, &c, 1);
}

int main( int argc, char **argv)
{
	if (argc == 2)
	{
		while (*argv[1])
			rot_13(*argv[1]++);
	}
	write (1, "\n", 1);
}
